package com.pactera.crawler.common.utils;

public class ValidaCodeException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1711015035165419022L;

	public ValidaCodeException() {
		super();
	}

	public ValidaCodeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ValidaCodeException(String message, Throwable cause) {
		super(message, cause);
	}

	public ValidaCodeException(String message) {
		super(message);
	}

	public ValidaCodeException(Throwable cause) {
		super(cause);
	}

}
